#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_divide(int d)
{
	char i;
	char j;

	i = (d / 10) + '0';
	j = (d % 10) + '0';
	ft_putchar(i);
	ft_putchar(j);
}

void	ft_print_comb2(void)
{
	int i;
	int j;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			ft_divide(i);
			ft_putchar(' ');
			ft_divide(j);
			if (!(i == 98 && j == 99))
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			j++;
		}
		i++;
	}
}
